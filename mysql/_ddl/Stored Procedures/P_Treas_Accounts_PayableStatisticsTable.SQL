        ,at_pda.EnterpriseRefundCount           -- その他未収金相殺(返金件数)
        ,at_pda.EnterpriseRefund                -- その他未収金相殺(返金)
        ,NULL                                   -- 売掛金(前月売掛金残)
        ,NULL                                   -- 売掛金(当月売上増加)
        ,NULL                                   -- 売掛金(決済手数料件数)
        ,NULL                                   -- 売掛金(決済手数料金額)
