        ,at_pda.EnterpriseAccountsDue           -- 売掛金(未収金へ振替)
        ,NULL                                   -- 売掛金(当月入金件数)
        ,NULL                                   -- 売掛金(当月入金)
        ,NULL                                   -- 売掛金(貸倒件数)
        ,NULL                                   -- 売掛金(貸倒金額)
        ,NULL                                   -- 売掛金(当月末売掛金残)
        ,NULL                                   -- 売掛金(売掛金合計)
