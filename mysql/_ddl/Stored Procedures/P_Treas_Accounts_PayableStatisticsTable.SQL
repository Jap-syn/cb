        ,NULL                                   -- 未収金(当月入金件数)
        ,NULL                                   -- 未収金(当月入金)
        ,NULL                                   -- 未収金(貸倒件数)
        ,NULL                                   -- 未収金(貸倒金額)
        ,NULL                                   -- 未収金(当月末未収金残)
        ,NULL                                   -- 未収金(現在未収金)
    FROM  AT_PayOff_DailyAccount at_pda
    WHERE DATE_FORMAT(at_pda.AdvancesDate, '%Y/%m/01') = v_AccountingMonth
    AND at_pda.OemId = 0
    ;

    -- ------------------------------
    -- ③日次売上明細(月額固定費)より作成
    -- ------------------------------
    -- 日次売上明細.会計月の年月 = 会計月の年月 AND 日次売上明細.OEMID = 0(直営) AND 日次売上明細.売上確定条件=8(月額固定費)の日次売上明細を抽出し直営未払金･売掛金･加盟店未収金統計表ﾜｰｸに出力する
    INSERT INTO AW_Accounts_PayableStatisticsTable (
         OemId                                  -- OEMID
        ,EnterpriseId                           -- 加盟店ID
        ,AP_AccountsPayableBalance              -- 未払金(前月未払金残高)
        ,AP_ChargeCount                         -- 未払金(当月発生件数)
        ,AP_ChargeAmount                        -- 未払金(当月発生金額)
        ,AP_CancelCount                         -- 未払金(当月ｷｬﾝｾﾙ件数)
        ,AP_CalcelAmount                        -- 未払金(当月ｷｬﾝｾﾙ立替金額)
