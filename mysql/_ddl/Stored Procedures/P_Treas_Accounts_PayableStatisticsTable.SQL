    SELECT
         at_dsd.OemId                           -- OEMID
        ,at_dsd.EnterpriseId                    -- 加盟店ID
        ,NULL                                   -- 未払金(前月未払金残高)
        ,NULL                                   -- 未払金(当月発生件数)
        ,NULL                                   -- 未払金(当月発生金額)
        ,NULL                                   -- 未払金(当月ｷｬﾝｾﾙ件数)
        ,NULL                                   -- 未払金(当月ｷｬﾝｾﾙ立替金額)
        ,NULL                                   -- 未払金(当月立替金戻し件数)
        ,NULL                                   -- 未払金(当月立替金戻し金額)
        ,NULL                                   -- 未払金(売掛金相殺)
        ,NULL                                   -- 未払金(未収金相殺)
        ,NULL                                   -- 未払金(その他未収金相殺)
