        ,IFNULL(at_dsd.NCreditNoticeMonthlyFee, 0) + IFNULL(at_dsd.NCreditNoticeMonthlyFeeTax, 0)     -- 売掛金(次回請求与信結果通知ｻｰﾋﾞｽ月額固定費)
