        ,NULL                                   -- 未払金(貸倒件数)
        ,NULL                                   -- 未払金(貸倒金額)
        ,NULL                                   -- 未払金(当月未払金残)
        ,NULL                                   -- 未払金(商品代金等合計)
        ,NULL                                   -- 未払金(差額)
        ,IFNULL(at_pda.StampFee, 0) + IFNULL(at_pda.TransferCommission, 0) + IFNULL(at_pda.AdjustmentAmount, 0) + IFNULL(at_pda.EnterpriseRefund, 0)            -- その他未収金相殺(合計)
        ,at_pda.StampFeeCount                   -- その他未収金相殺(印紙代件数)
        ,at_pda.StampFee                        -- その他未収金相殺(印紙代)
