        ,NULL                                   -- その他未収金相殺(印紙代)
        ,NULL                                   -- その他未収金相殺(振込手数料件数)
        ,NULL                                   -- その他未収金相殺(振込手数料)
        ,NULL                                   -- その他未収金相殺(調整金額件数)
        ,NULL                                   -- その他未収金相殺(調整金額)
        ,NULL                                   -- その他未収金相殺(返金件数)
        ,NULL                                   -- その他未収金相殺(返金)
        ,NULL                                   -- 売掛金(前月売掛金残)
        ,NULL                                   -- 売掛金(当月売上増加)
        ,NULL                                   -- 売掛金(決済手数料件数)
        ,NULL                                   -- 売掛金(決済手数料金額)
        ,NULL                                   -- 売掛金(請求手数料件数)
        ,NULL                                   -- 売掛金(請求手数料金額)
        ,IFNULL(at_dsd.MonthlyFee, 0) + IFNULL(at_dsd.MonthlyFeeTax, 0)                               -- 売掛金(月額固定費)
        ,IFNULL(at_dsd.IncludeMonthlyFee, 0) + IFNULL(at_dsd.IncludeMonthlyFeeTax, 0)                 -- 売掛金(同梱月額固定費)
