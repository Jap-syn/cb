    /**
     * 入金取消し可能か否か
     * @param unknown $key
     * @param unknown $value
     * @return boolean
     */
    public function isReceiptCancel($key, $value, $cntDouble) {

        $mdlo = new TableOrder($this->dbAdapter);

        // 注文SEQが存在すること
        $row = $mdlo->find($key)->current();
        if (!$row) {
            // データが取得出来ない場合
            $this->logger->alert('[DoubleCheck]' . "\t" . $key . "\t" . $value . "\t" . 'OrderSeq Is Not Found');
            return false;
        }

        // キャンセルされていないこと
        if ($mdlo->isCanceled($key)) {
            $this->logger->alert('[DoubleCheck]' . "\t" . $key . "\t" . $value . "\t" . 'Is Cancel');
            return false;
        }

        // 一部入金もしくは、入金クローズではない
        if ( !(($row['DataStatus'] == 61) || ($row['DataStatus'] == 91 && $row['CloseReason'] == 1)) ) {
            $this->logger->alert('[DoubleCheck]' . "\t" . $key . "\t" . $value . "\t" . 'DataStatus Is InValid[DataStatus=' . $row['DataStatus'] . ',CloseReason=' . $row['CloseReason'] . ']');
            return false;
        }


        // 返金されていないこと
        $sql = ' SELECT COUNT(1) AS CNT FROM T_ClaimControl cc,T_RepaymentControl rc WHERE cc.ClaimId = rc.ClaimId AND cc.OrderSeq = :OrderSeq AND rc.RepayStatus IN (0, 1) ';
        $row = $this->dbAdapter->query($sql)->execute(array(':OrderSeq' => $key))->current();
        $cnt = (int)$row['CNT'];
        if ($cnt > 0) {
            // 返金済み
            $this->logger->alert('[DoubleCheck]' . "\t" . $key . "\t" . $value . "\t" . 'Is Repayment Input');
            return false;
        }

        // 手動の雑損失、雑収入がないこと
        $sql = ' SELECT COUNT(1) AS CNT FROM T_SundryControl sc WHERE sc.OrderSeq = :OrderSeq AND sc.SundryClass <> 99 ';
        $row = $this->dbAdapter->query($sql)->execute(array(':OrderSeq' => $key))->current();
        $cnt = (int)$row['CNT'];
        if ($cnt > 0) {
            // 雑損失または雑収入の入力あり
            $this->logger->alert('[DoubleCheck]' . "\t" . $key . "\t" . $value . "\t" . 'Is Sundry Input');
            return false;
        }

        // 入金額×$cntDoubleであること
        $sql = ' SELECT SUM(rc.ReceiptAmount) AS SUM, COUNT(1) AS CNT FROM T_ReceiptControl rc WHERE rc.OrderSeq = :OrderSeq ';
        $row = $this->dbAdapter->query($sql)->execute(array(':OrderSeq' => $key))->current();
        $sum = (int)$row['SUM'];
        $cnt = (int)$row['CNT'];

        // 入金件数が3件ではない
        if ( $cnt <> $cntDouble ) {
            $this->logger->alert('[DoubleCheck]' . "\t" . $key . "\t" . $value . "\t" . 'Not ' . $cntDouble . 'Count');
            return false;
        }

        // 入金額が3倍ではない
        if ( !($sum == ($value * $cntDouble)) ) {
            $this->logger->alert('[DoubleCheck]' . "\t" . $key . "\t" . $value . "\t" . 'Not Summary Amount');
            return false;
        }

        return true;
    }

